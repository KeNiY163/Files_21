package model;

public class Cars {

    private int id;
    private String brand;
    private String model;
    private String country;
    private int year;
    private int price;

    public int getId() {
        return id;
    }

    public String getBrand() {
        return brand;
    }

    public String getModel() {
        return model;
    }

    public String getCountry() {
        return country;
    }

    public int getYear() {
        return year;
    }

    public int getPrice() {
        return price;
    }

}
